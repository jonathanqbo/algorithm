public class BitUsage {

    public static void main(String[] args) {
        int i1 = 10;
        int i2 = 10 << 1; // == 10 * 2
        int i3 = 10 >> 1; // == 10 / 2
        System.out.println(i2);
        System.out.println(i3);
    }
}
